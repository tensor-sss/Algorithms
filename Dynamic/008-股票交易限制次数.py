# 最多可以交易两次

def func2(l1):
    if not l1:
        return 0

    # 构建二维数组，每天有 5 个状态
    dp = [[0, 0, 0, 0, 0] for i in range(len(l1))]

    for i in range(len(l1)):
        if i == 0:
            # 第一天（索引为0）的初始化状态
            dp[0][0] = 0  # 无操作
            dp[0][1] = -l1[i]  # 第一次买入，花钱了所以是负数
            dp[0][2] = 0  # 第一天买入又卖出，利润为0
            dp[0][3] = -l1[i]  # 第一天买入、卖出，再买入（相当于只做了一次买入），资金同样为 -l1[i]
            dp[0][4] = 0  # 第一天买卖两次，利润为0
        else:
            # dp[i][0] = dp[i-1][0] (因为永远是0，这行可以省略，但为了对齐逻辑写出来)
            dp[i][0] = dp[i - 1][0]

            # 第一次买入：保持之前的买入状态，或者今天刚买入（从状态0花钱买）
            dp[i][1] = max(dp[i - 1][1], dp[i - 1][0] - l1[i])

            # 第一次卖出：保持之前的卖出状态，或者今天卖出（从状态1加上今天卖出的钱）
            dp[i][2] = max(dp[i - 1][2], dp[i - 1][1] + l1[i])

            # 第二次买入：保持之前的第二买入状态，或者今天刚买入（从状态2花钱买）
            dp[i][3] = max(dp[i - 1][3], dp[i - 1][2] - l1[i])

            # 第二次卖出：保持之前的第二卖出状态，或者今天卖出（从状态3加上今天卖出的钱）
            dp[i][4] = max(dp[i - 1][4], dp[i - 1][3] + l1[i])

    # 最终结果一定是最后一天“第二次卖出”状态的金额最大
    return dp[-1][4]


l1 = [8, 3, 5, 10, 2, 1, 5, 2]
res = func2(l1)
print(res)  # 7
